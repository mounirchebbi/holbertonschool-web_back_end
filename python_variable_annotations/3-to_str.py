#!/usr/bin/env python3
"""
3-to_str.py

This module to convert a float number to its string representation.
"""


def to_str(n: float) -> str:
    """
    This function takes a float and returns its string representation.

    Parameters:
        n (float)
    Returns:
        str: The string representation of the float n.
    """
    return str(n)
