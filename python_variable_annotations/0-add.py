#!/usr/bin/env python3
"""
0-add.py
This module provides a function to perform addition of two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    This function takes two float numbers and returns their sum.

    Parameters:
        a (float)
        b (float)

    Returns:
        float: The sum of a and b.
    """
    return a + b
