"""
1-concat.py

This module provides a function to concatenate two strings.
"""


def concat(str1: str, str2: str) -> str:
    """
    This function takes two strings and returns their concatenation.

    Parameters:
        str1 (str)
        str2 (str)

    Returns:
        str: The concatenated result of str1 and str2.
    """
    return str1 + str2
